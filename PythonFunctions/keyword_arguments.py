# You can also send arguments with the key = value syntax.

def my_function(sibling3, sibling2, sibling1):
    print("The youngest sibling is " + sibling3)
    print("The middle sibling is " + sibling2)
    print("The oldest sibling is " + sibling1)

my_function(sibling1 = "Joseph", sibling2 = "Dennis", sibling3 = "Magdalene") 